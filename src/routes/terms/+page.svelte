		<h1>Humming Studio 利用規約</h1>
		<p class="updated">最終更新日: 2026年7月16日</p>
		<p>この利用規約(以下「本規約」)は、合同会社ツナギビト(以下「当社」)が提供するアプリケーション「Humming Studio」および関連するホスティング・配信サービス(以下あわせて「本サービス」)の利用条件を定めるものです。ユーザーは、本アプリの初回起動時の同意画面での同意、または本サービスの利用の開始により、本規約に同意したものとみなされます。とりわけ<strong>第5条(提携メディアへの掲載)</strong>は、ユーザーが公開したコンテンツが外部の媒体で配信される可能性に関する重要な条項です。よくお読みください。</p>

		<h2>第1条(定義)</h2>
		<ol>
		<li>「本アプリ」とは、iPadOS / iOS / macOS 向けアプリケーション「Humming Studio」をいいます。</li>
		<li>「ホスティング機能」とは、ユーザーが本アプリで制作した音声・映像コンテンツを当社サーバーに保存し、RSS フィード等を通じて配信する機能をいいます。</li>
		<li>「公開」とは、ユーザーがホスティング機能を用いて番組またはエピソードを配信可能な状態にすることをいいます。</li>
		<li>「ユーザーコンテンツ」とは、ユーザーが本サービスにアップロードまたは公開した音声、映像、テキスト、画像、番組情報(タイトル・説明・チャプター等のメタデータを含む)その他一切のコンテンツをいいます。</li>
		<li>「提携メディア」とは、当社が運営または提携する以下の各媒体を総称していいます。当社は、提携メディアを追加・変更することがあり、その場合は本ページ上で告知します。
			<ul>
			<li>「ラジオ経済」(radio-keizai.com。ミニFM およびインターネットラジオ)</li>
			<li>「CANDY WAVE」(インターネットラジオ)</li>
			<li>「GREEN WAVE」(インターネットラジオ)</li>
			</ul>
		</li>
		<li>「RSS フィード」とは、公開された番組のエピソード情報を配信するための、当社が発行する更新情報ファイル(RSS 2.0 形式)をいいます。</li>
		</ol>

		<h2>第2条(アカウント)</h2>
		<ol>
		<li>ホスティング機能の利用には、Apple アカウントによるサインイン(Sign in with Apple)が必要です。録音・編集・書き出しなど端末内で完結する機能は、アカウントなしで利用できます。</li>
		<li>ユーザーは、自己のアカウントを第三者に利用させてはなりません。アカウントの管理はユーザーの責任で行うものとします。</li>
		<li>ユーザーは、本サービスを利用する権利能力を有し、未成年者・成年被後見人等である場合には法定代理人の同意を得ていることを表明します。</li>
		<li>ユーザーはいつでもアプリ内の操作によりアカウントを削除できます。削除後、ホスティング中のコンテンツおよび RSS フィードは 30 日間の猶予期間の後に削除されます。</li>
		</ol>

		<h2>第3条(料金・サブスクリプション)</h2>
		<ol>
		<li>本アプリの基本機能(録音・文字起こし・ブロック編集・書き出し等、端末内で完結する機能)は無料で利用できます。書き出しの時間制限解除、クラウド文字起こし・AI 機能、複数番組の運用、およびホスティング機能は、有料プラン「Humming Studio Pro」(自動更新サブスクリプション、月額600円・税込)または買い切りプラン「Humming Studio Unlock」の特典として提供されます。</li>
		<li>決済は Apple の App Store を通じて行われ、解約・返金は Apple の定める条件に従います。サブスクリプションは、現在の期間終了の 24 時間前までに解約しない限り自動更新されます。解約は App Store のアカウント設定からいつでも行えます。</li>
		<li>サブスクリプション終了後は新規の公開ができなくなります。公開済みコンテンツの配信は、終了後 90 日間は維持され、その後停止されることがあります。</li>
		</ol>

		<h2>第4条(ホスティング機能・ライセンス)</h2>
		<ol>
		<li>ストレージ容量はユーザーあたり 10GB、1エピソードあたり 500MB を上限とします(予告のうえ変更することがあります)。</li>
		<li>ユーザーコンテンツの著作権その他の権利は、ユーザーまたは正当な権利者に帰属します。当社はユーザーコンテンツの権利を取得しません。</li>
		<li>ユーザーは当社に対し、本サービスの提供に必要な範囲(保存、複製、形式変換・エンコード、RSS フィードによる配信、サムネイル生成、ならびに第5条に定める提携メディアでの掲載・放送・ストリーミング配信)で、ユーザーコンテンツを利用する、無償・非独占的で、地域の限定のない、サブライセンス可能なライセンスを許諾するものとします。本ライセンスは、当該コンテンツの公開停止・削除により、合理的な期間内かつ技術的に可能な範囲で終了します(既に配信・キャッシュされた複製物についてはこの限りではありません)。</li>
		<li>RSS フィードの URL は番組作成時に確定し、以後変更できません(番組名の改称等による URL 切替のリスクを避けるためです)。</li>
		</ol>

		<h2>第5条(提携メディアへの掲載)</h2>
		<ol>
		<li>ホスティング機能で公開された番組は、当社が運営または提携する<strong>提携メディア(ラジオ経済 / CANDY WAVE / GREEN WAVE)</strong>において、RSS フィードを通じて自動的に取り込まれ、紹介・掲載・番組表への収録・ストリーミング配信・放送(あわせて「掲載」)されることがあります。</li>
		<li>ユーザーは、本アプリ初回起動時の同意画面において、公開した番組が提携メディアに掲載されることに同意するかを選択できます。掲載の要否は<strong>番組ごとに</strong>設定でき、ユーザーはアプリ内の番組設定からいつでも掲載を開始・停止できます。掲載を停止した場合、以後の新規取り込みは行われません(提携メディア側に既に取り込まれた情報の反映停止には時間を要する場合があります)。</li>
		<li>掲載の対象となるのは、RSS フィードに含まれる番組情報・エピソード・音声(映像)であり、当社はこれ以外の目的でユーザーコンテンツを第三者に提供しません。</li>
		<li>提携メディアでの掲載・放送・編成の有無および内容は当社および各媒体の裁量により決定され、当社は掲載・放送を保証しません。掲載によりユーザーに対価が支払われるものではありません。</li>
		<li>ユーザーは、提携メディアで掲載されることを前提に、ユーザーコンテンツについて必要な権利処理(第三者の音源・楽曲・素材の利用許諾を含む)を自己の責任で行うものとします。</li>
		</ol>

