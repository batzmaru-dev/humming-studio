		<p>ユーザーは、公開するユーザーコンテンツについて、次の各号を表明し保証します。</p>
		<ol>
		<li>ユーザーが正当な権利を有するか、必要な許諾を得ていること。</li>
		<li>第三者の著作権・著作者人格権・商標権・肖像権・パブリシティ権・プライバシーその他の権利を侵害しないこと。</li>
		<li>本サービスおよび提携メディアでの配信について、共演者・出演者・権利者から必要な同意を得ていること。</li>
		</ol>

