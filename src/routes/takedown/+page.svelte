		<h1>削除対応ポリシー(権利者の方へ)</h1>
		<p class="updated">最終更新日: 2026年7月12日</p>
		<p>合同会社ツナギビト(以下「当社」)は、Humming Studio のホスティング機能および「ラジオ経済」で配信されるコンテンツについて、著作権その他の権利を侵害するコンテンツの申立てに対し、以下の手続で対応します。</p>
		<h2>1. 申立ての方法</h2>
		<p>権利侵害を発見された権利者(または正当な代理人)の方は、次の情報を添えて下記窓口までご連絡ください。</p>
		<ul>
		<li>申立人の氏名・名称、連絡先(メールアドレス)</li>
		<li>権利を侵害されているコンテンツの URL(RSS フィードまたはエピソードの URL)</li>
		<li>侵害されている権利の内容(著作物の特定を含む)</li>
		<li>申立人が権利者本人または正当な代理人であることの表明</li>
		</ul>
		<p>窓口: kawakami@tsunagu-hito.com(件名に「削除申立て」と明記してください)</p>
		<h2>2. 対応の流れ</h2>
		<ol>
		<li>申立てを受領後、原則 5 営業日以内に内容を確認します。</li>
		<li>侵害の蓋然性が高いと判断した場合、対象コンテンツの配信を<strong>一時停止</strong>します。</li>
		<li>コンテンツを公開したユーザーに申立ての内容を通知し、<strong>7 日間の反論期間</strong>を設けます。</li>
		<li>反論がない場合、または反論に理由がないと判断した場合、コンテンツを削除します。正当な反論があった場合は配信を再開することがあります。</li>
		<li>結果は申立人・ユーザー双方に通知します。</li>
		</ol>
		<h2>3. 繰り返しの侵害</h2>
		<p>権利侵害を繰り返すユーザーに対しては、利用規約に基づきアカウントの停止・削除を行います。</p>
		<h2>4. 虚偽の申立て</h2>
		<p>故意に虚偽の申立てを行った場合、それにより生じた損害について申立人が責任を負うことがあります。</p>
